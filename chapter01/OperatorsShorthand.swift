import Foundation

var counter: Int = 8
print(counter)

// Shorthand for increment
counter += 1
print(counter)  // 9

// Shorthand for decrement
counter -= 1
print(counter)  // 8

// Shorthand for multiplication
counter *= 3
print(counter)  // 24

// Shorthand for division
counter /= 2
print(counter)  // 12
